document.addEventListener('DOMContentLoaded', function() {
    const inputEl = document.getElementById('asciiInput');
    if (inputEl) {
        inputEl.addEventListener('keydown', function(e) {
            if (e.key === 'Enter') {
                e.preventDefault();
                generateAscii();
            }
        });
    }

    generateAscii();
    renderFontPreview();
});

function generateAscii() {
    const text = document.getElementById('asciiInput').value.toUpperCase().trim();
    const fontName = document.getElementById('fontStyle').value;
    const mirror = document.getElementById('mirrorCheck').checked;
    const flip = document.getElementById('flipCheck').checked;

    if (!text) {
        document.getElementById('asciiOutput').innerHTML = '<div class="result-empty">输入文字后点击"生成"按钮查看 ASCII 艺术字效果</div>';
        return;
    }

    const font = ASCII_FONTS[fontName];
    if (!font) {
        showToast('未找到指定字体', 'error');
        return;
    }

    let resultLines = Array(font.height).fill('');

    for (const char of text) {
        if (font.chars[char]) {
            const charArt = font.chars[char];
            for (let i = 0; i < font.height; i++) {
                resultLines[i] += (charArt[i] || '') + ' ';
            }
        } else if (char === ' ') {
            for (let i = 0; i < font.height; i++) {
                resultLines[i] += '  ';
            }
        } else {
            for (let i = 0; i < font.height; i++) {
                resultLines[i] += '? ';
            }
        }
    }

    if (mirror) {
        resultLines = resultLines.map(line => line.split('').reverse().join(''));
    }

    if (flip) {
        resultLines = resultLines.reverse();
    }

    const output = document.getElementById('asciiOutput');
    output.innerHTML = '<code>' + resultLines.map(line => Utils.escapeHtml(line)).join('\n') + '</code>';
}

function renderFontPreview() {
    const container = document.getElementById('fontPreviewAll');
    if (!container) return;

    const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    container.innerHTML = '';

    Object.keys(ASCII_FONTS).forEach(fontName => {
        const font = ASCII_FONTS[fontName];
        let previewText = `<div style="margin-bottom:12px;"><strong style="font-size:12px;color:var(--primary-color);">${fontName}</strong></div><pre style="background:#fafafa;padding:10px;border-radius:6px;font-size:10px;line-height:1.2;margin:0;overflow-x:auto;">`;

        for (let row = 0; row < font.height && row < 3; row++) {
            let line = '';
            for (const char of alphabet.substring(0, 13)) {
                if (font.chars[char] && font.chars[char][row]) {
                    line += font.chars[char][row] + ' ';
                } else {
                    line += '    ';
                }
            }
            previewText += Utils.escapeHtml(line) + '\n';
        }
        previewText += '</pre>';
        container.innerHTML += previewText;
    });
}

function copyAscii() {
    const output = document.getElementById('asciiOutput');
    const text = output.textContent.trim();

    if (!text || text.includes('输入文字后')) {
        showToast(I18N.t('common.error'), 'error');
        return;
    }

    copyToClipboard(text, I18N.t('common.copied'));
}

function downloadAscii() {
    const output = document.getElementById('asciiOutput');
    const text = output.textContent.trim();

    if (!text || text.includes('输入文字后')) {
        showToast(I18N.t('common.error'), 'error');
        return;
    }

    downloadFile(text, 'ascii-art.txt', 'text/plain;charset=utf-8');
}

